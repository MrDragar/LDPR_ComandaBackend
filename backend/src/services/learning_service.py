class LearningService(ILearningService):
    def __init__(self, uow: IUnitOfWork, repo: ILearningRepository, user_svc: IUserService,
                 balance_svc: IBalanceService):
        self.__uow = uow
        self.__repo = repo
        self.__user_svc = user_svc
        self.__balance_svc = balance_svc

    async def get_question(self, question_index: int) -> tuple[str, list[str], int]:
        if not 0 <= question_index < len(_QUESTIONS_REAL):
            raise DomainError("Вопрос не найден")

        q_data = _QUESTIONS_REAL[question_index]
        q_text = q_data[0]
        options = q_data[1]
        correct_idx = q_data[2]

        # Возвращаем текст, варианты и индекс правильного ответа
        return q_text, options, correct_idx

    async def finish_quiz(self, user_id: int, user_source: Sources, score: int) -> dict:
        async with self.__uow.atomic():
            prev = await self.__repo.get_attempt(user_id, user_source)
            is_first_pass = not prev or not prev.is_passed

            now = datetime.now()
            attempt = LearningTestAttempt(
                user_id=user_id, user_source=user_source,
                score=score, passed_at=now, is_passed=score >= 7
            )
            await self.__repo.save_attempt(attempt)
            if score >= 7:
                if not is_first_pass:
                    return {
                        "status": "success_repeat",
                        "message": "Поздравляю, вы успешно прошли тест. Поскольку вы его уже "
                                   "проходили, вы не получаете за него баллы"
                    }
                else:
                    await self.__balance_svc.add_balance(user_id, user_source, 5,
                                                         "Прохождение обучения")
                    msg = "Поздравляем, вы успешно прошли тест. Вам начислено 5 баллов\n"
                    return {"status": "success_first", "message": msg}
            else:
                return {
                    "status": "fail",
                    "message": "К сожалению, вы не прошли этот тест. Но вы всегда можете пройти его ещё раз"
                }

    async def is_learning_passed(self, user_id: int, user_source: Sources) -> bool:
        async with self.__uow.atomic():
            return await self.__repo.is_passed(user_id, user_source)